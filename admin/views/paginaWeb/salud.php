          <div class="row">
            <div class="col-md-1"></div>
            <div class="col-md-10 pt-3">
              <ol id="lista2">
                <li>
                  Los materiales almacenados deben estar alejados de bordes o
                  excavaciones
                </li>
                <li>
                  Bajo ninguna circunstancia se deben dejar herramientas o
                  materiales estorbando el paso, esto puede provocar molestias y
                  accidentes
                </li>
                <li>
                  No permitas líquidos inflamables ni cigarros en la zona de
                  trabajo
                </li>
                <li>
                  Los trabajos que se lleven a cabo a más de 3.5 metros de
                  altura, se deben realizar con protecciones especiales: si es
                  posible, colocar redes, cuerdas y cinturones de seguridad
                </li>
                <li>
                  Se prohíbe el trabajo en andamios cuando haya vientos fuertes
                </li>
                <li>
                  En los andamios sólo deben estar las personas que estarán
                  trabajando ahí, y se debe evitar pasar por debajo de ellos
                </li>
                <li>
                  Si existen huecos horizontales, deben cubrirse con madera o
                  metal para evitar posibles caídas
                </li>
                <li>
                  Se debe cuidar la postura al levantar objetos pesados: siempre
                  doblar las rodillas para levantarlos del suelo, tomar
                  firmemente el objeto, acercarlo al cuerpo, y levantarse
                  utilizando la fuerza en piernas
                </li>
                <li>
                  Cada herramienta es útil para tareas específicas, asegúrate de
                  que la herramienta que estás utilizando sea adecuada para el
                  trabajo
                </li>
                <li>
                  Los aparatos eléctricos deben estar lejos de zonas donde haya
                  agua o fuego, y debes asegurarte de que tanto cables como
                  conexiones estén en perfecto estado
                </li>
                <li>
                  Procura tener a la mano un botiquín de primeros auxilios
                </li>
              </ol>
